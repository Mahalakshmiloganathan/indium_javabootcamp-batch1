package calculatorapp;


public class CalculatorApp {
    public static void main(String[] args) {

        int value1 = 10;
        int value2 = 2;

        //Adition
        int add = value1+value2;
        System.out.println("Addition of value1 + value2 = " + add);

        //subtraction
        int sub = value1-value2;
        System.out.println("Subtraction of value1 - value2 = " + sub);

        //Multiplication
        int mul = value1 * value2;
        System.out.println("Multiplication of value1 * value2 = " + mul);

        //division
        int div = value1 / value2;
        System.out.println("Division of value1 / value2 = " + div);

    }
}
